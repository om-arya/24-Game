    /**
     * Create a new deck.
     * @param hasEasy Include in the deck?
     * @param hasMedium Include in the deck?
     * @param hasHard Include in the deck?
     * @return A new deck.
     */
    public Card[] initializeDeck(boolean hasEasy, boolean hasMedium, boolean hasHard) {
        Card[] deck;

        int tot_count = 0;
        int offset = 0;

        if (hasEasy)
            tot_count += EASY_COUNT;
        if (hasMedium)
            tot_count += MEDIUM_COUNT;
        if (hasHard)
            tot_count += HARD_COUNT;

        deck = new Card[tot_count];

        if (hasEasy) {
            System.arraycopy(cards, 0, deck, 0, EASY_COUNT);
            offset += EASY_COUNT;
        }
        if (hasMedium) {
            System.arraycopy(cards, EASY_COUNT, deck, offset, MEDIUM_COUNT);
            offset += MEDIUM_COUNT;
        }
        if (hasHard)
            System.arraycopy(cards, MEDIUM_COUNT + EASY_COUNT, deck, offset, HARD_COUNT);

        return deck;
    }

